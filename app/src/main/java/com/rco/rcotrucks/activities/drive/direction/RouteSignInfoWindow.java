package com.rco.rcotrucks.activities.drive.direction;

public class RouteSignInfoWindow {

    private String instruction;
    private String manoeuvreSign;
    private float  rotation;


    public String getInstruction() {
        return instruction;
    }

    public void setInstruction(String instruction) {
        this.instruction = instruction;
    }

    public String getManoeuvreSign() {
        return manoeuvreSign;
    }

    public void setManoeuvreSign(String manoeuvreSign) {
        this.manoeuvreSign = manoeuvreSign;
    }

    public float getRotation() {
        return rotation;
    }

    public void setRotation(float rotation) {
        this.rotation = rotation;
    }
}
